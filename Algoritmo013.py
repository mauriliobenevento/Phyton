def pais(idioma):
    if idioma == 'es':
        print('Hola')
    elif idioma == 'fr':
        print('Bonjour')
    elif idioma == 'br':
        print('Olá')
    elif idioma == 'en':
        print('Hello')
    else:
        print('Hi')

pais('es')
pais('fr')
pais('br')
pais('en')
pais('')
