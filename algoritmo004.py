x = 10

if x == 10:
    print('É igual a 10')

if x < 10:
    print('É menor que 10')

if x > 10:
    print('É maior que 10')


if x <= 10:
    print('É menor ou igual a 10')

if x >= 10:
    print('É maior ou igual a 10')

if x != 10:
    print('Não é igual a 10')

