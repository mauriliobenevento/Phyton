texto = 'aprender a aprender é um esforço diário'

print(texto[0:3]) # da posição 0 mostre 3 caracteres
print(texto[4:5]) # da posição 4 mostre o proximo caractere
print(texto[20:40]) # da posiçào 20 mostre todo o resto da string
print(texto[:4]) # da posição inicial ignorada mostre 4 caracteres
print(texto[30:]) # da posição 30 mostre o resto da string
print(texto[:]) # mostre toda a string
